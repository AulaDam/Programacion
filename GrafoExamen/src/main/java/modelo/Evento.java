package modelo;

import java.util.Objects;

/**
 *
 * @author Enrique
 */
public class Evento {
    
    public int id;
    public String texto;

    public Evento(int id, String texto) {
        this.id=id;
        this.texto = texto;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 47 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Evento other = (Evento) obj;
        return this.id == other.id;
    }
    
    

  
    
    
}
